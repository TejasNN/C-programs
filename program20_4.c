///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//																			                                           
//	Problem statement :	Accept character from user and check whether it is small case or not. (a-z) 
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		CheckSmall
//	Input :				character
//	Output :			boolean
// 	Description :		Returns true if the input character is a small case.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				21/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

bool CheckSmall(char ch)
{
	bool bFlag = false;
	
	if((ch >= 'a') && (ch <= 'z'))
	{
		bFlag = true;
	}
	return bFlag;
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	char cValue = '\0';
	bool bRet = false;
	
	printf("Enter a character : \n");
	scanf("%c",&cValue);
	
	bRet = CheckSmall(cValue);
	
	if(bRet == true)
	{
		printf("It is a small case character\n");
	}
	else
	{
		printf("It is not a small case character\n");
	}
	
	return 0;
}

