#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		CountCapital
//	Input :				string
//	Output :			integer
// 	Description :		Returns count of capital letters from input string.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				22/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountCapital(char *str)
{
    int iCount = 0;

    while(*str != '\0')
    {
        if((*str >= 'A') && (*str <= 'Z'))
        {
           iCount++; 
        }
        str++;
    }
    return iCount;
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	char Arr[20];
    int iRet = 0;
	
	printf("Enter a string : \n");
	scanf("%[^'\n']s",Arr);
	
	iRet = CountCapital(Arr);

    printf("%d\n",iRet);

	return 0;
}

// Time complexity : O(N)
// where N is the number of characters in input string