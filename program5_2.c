#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		Display
//	Input :				integer
//	Output :			void
// 	Description :		Prints numbers till input number  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				04/05/2023
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
	int iCnt = 0;
	
	for(iCnt = 1; iCnt <= iNo; iCnt++)
	{
		printf("%d\t",iCnt);
	}
}
	

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	
	printf("Enter number : \n");
	scanf("%d",&iValue);
	
	Display(iValue);
	
	return 0;
}

