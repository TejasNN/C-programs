///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//																			                                           
//	Problem statement :	Accept character from user and check whether it is capital or not. (A-Z) 
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		CheckCapital
//	Input :				character
//	Output :			boolean
// 	Description :		Returns true if the input character is a capital letter.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				21/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

bool CheckCapital(char ch)
{
	bool bFlag = false;
	
	if((ch >= 'A') && (ch <= 'Z'))
	{
		bFlag = true;
	}
	return bFlag;
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	char cValue = '\0';
	bool bRet = false;
	
	printf("Enter a character : \n");
	scanf("%c",&cValue);
	
	bRet = CheckCapital(cValue);
	
	if(bRet == true)
	{
		printf("%c is a capital letter\n",cValue);
	}
	else
	{
		printf("%c is not a capital letter\n",cValue);
	}
	
	return 0;
}

