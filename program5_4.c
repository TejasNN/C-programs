//																			                                           
//	Problem statement : Write a program which accepts N number from user and print all odd numbers upto N.              
//																			                                        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		OddDisplay
//	Input :				integer
//	Output :			void
// 	Description :		Prints all odd numbers upto input number  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				04/05/2023
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////////

void OddDisplay(int iNo)
{
	int iCnt = 0;
	
	for(iCnt = 1; iCnt <= iNo; iCnt++)
	{
		if((iCnt % 2) != 0)
		{
			printf("%d\t",iCnt);
		}
	}
}
	

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	
	printf("Enter number : \n");
	scanf("%d",&iValue);
	
	OddDisplay(iValue);
	
	return 0;
}

// Time complexity : N
// Where N is natural number
