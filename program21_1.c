//	Problem statement :	Write a program which displays ASCII table.Table contains symbol, decimal, hexadecimal and octal
//						representation of every member from 0 to 255.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>
#include<stdbool.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		DisplayASCII
//	Input :				None
//	Output :			void
// 	Description :		display ASCII table.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				21/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayASCII()
{
	int iCnt = 0;
	for(iCnt = 0; iCnt <= 255; iCnt++)
	{
		printf("%d	%x 	%o 	%c	\n",iCnt,iCnt,iCnt,iCnt);
	}
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	DisplayASCII();
	
	return 0;
}

