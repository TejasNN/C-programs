#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		CountWhite
//	Input :				string
//	Output :			integer
// 	Description :		Accepts input string and return the count of white spaces.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				24/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountWhite(char *str)
{
    int iCnt = 0;

    while(*str != '\0')
    {
        if(*str == ' ')
        {
            iCnt++;
        }
        str++;
    }
    return iCnt;
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50];
    int iRet = 0;

    printf("Enter string : \n");
    scanf("%[^'\n']s",Arr);

    iRet = CountWhite(Arr);

    printf("%d\n",iRet);

    return 0;
}

// Time complexity : O(N)
// where N is the number of characters in input string