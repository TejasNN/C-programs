//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//																			                                           
//	Problem statement :	Accept character from user and display its ASCII value in decimal, octal and hexadecimal format.
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		Display
//	Input :				character
//	Output :			void
// 	Description :		Displays input characters decimal,octal and hexadecimal values.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				22/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
	printf("Decimal     %d\nOctal       %o\nHexadecimal %x\n",ch,ch,ch);
}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	char cValue = '\0';
	
	printf("Enter a character : \n");
	scanf("%c",&cValue);
	
	Display(cValue);

	return 0;
}

