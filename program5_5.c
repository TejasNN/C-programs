//	Problem statement : Write a program which accepts N and print first 5 multiples of N.              
//																			                                        
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		MultipleDisplay
//	Input :				integer
//	Output :			void
// 	Description :		Prints first 5 multiples of input number  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				04/05/2023
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////////

void MultipleDisplay(int iNo)
{
	int iCnt = 0;
	int iMultiple = 0;
	
	for(iCnt = 1; iCnt <= 5; iCnt++)
	{
		iMultiple = iMultiple + iNo;
		printf("%d\t",iMultiple);
	}
}
	

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	
	printf("Enter number : \n");
	scanf("%d",&iValue);
	
	MultipleDisplay(iValue);
	
	return 0;
}

// Time complexity : N
// Where N is natural number
