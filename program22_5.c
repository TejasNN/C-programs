#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Function name :		Reverse
//	Input :				string
//	Output :			void
// 	Description :		Displays input string in reverse.  
// 	Author :			Tejas Nandakumar Nagvekar
// 	Date :				22/05/2023
// 
///////////////////////////////////////////////////////////////////////////////////////////////////////////

void Reverse(char *str)
{
    char *start = NULL;

    start = str;

    while(*str != '\0')
    {
        str++;
    }
    
    str--;

    while(start <= str)
    {
        printf("%c",*str);
        str--;
    }

}

/////////////////////////////////////////////////////////////////////////////////
//	Entry point function													   //
/////////////////////////////////////////////////////////////////////////////////

int main()
{
	char Arr[20];

	printf("Enter a string : \n");
	scanf("%[^'\n']s",Arr);
	
	Reverse(Arr);

	return 0;
}

// Time complexity : O(N)
// where N is the number of characters in input string